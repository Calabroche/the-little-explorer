/** Resolve a dotted key path against a dictionary. */
// eslint-disable-next-line @typescript-eslint/no-explicit-any
function resolve(d: any, path: string): string {
  return path.split('.').reduce((acc, k) => (acc != null ? acc[k] : undefined), d) ?? path;
}

/** Translate a key (dotted path) with optional {var} interpolation. */
export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  let s = resolve(dict[lang], key) || resolve(dict.fr, key) || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return s;
}
